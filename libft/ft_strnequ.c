#include "libft.h"

int		ft_strnequ(const char *s1, const char *s2, size_t n)
{
	if (!s1 || !s2)
		return (0);
	if (n == 0)
		return (1);
	while (n && *s1 && *s2)
	{
		if (*s1++ != *s2++)
			return (0);
		n--;
	}
	return (1);
}
